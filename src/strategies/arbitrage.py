"""
Arbitrage Strategy Module
Strategy 1: Auto-trade mispriced markets

Primary signal: Quantitative log-normal model driven by live OHLCV data (ATR-based vol).
Secondary signal: AI confirms or adjusts the quant estimate (optional, 30% weight).

For non-crypto markets the AI is still used as the primary estimator (no quant signal).
If AI is unavailable AND the market is not a crypto price market, the market is skipped.
"""

import logging
import math
import re
from typing import Dict, List, Optional, Any, Tuple
from dataclasses import dataclass
from datetime import datetime, timezone

from ..market.scanner import Market
from ..analysis.ai_agent import AIAnalysis, AIAgent
from ..analysis.math_utils import PositionSizer, calculate_arbitrage_profit

logger = logging.getLogger(__name__)


@dataclass
class TradeSignal:
    """Trading signal generated by strategy"""

    market_id: str
    token_id_yes: str
    token_id_no: str
    market_question: str
    end_date: Optional[datetime]
    action: str  # "BUY_YES", "BUY_NO"
    price: float
    size: float
    edge: float
    ai_confidence: float
    reasoning: str
    timestamp: datetime
    auto_execute: bool = True


# ---------------------------------------------------------------------------
# Quantitative probability engine
# ---------------------------------------------------------------------------

class CryptoPriceAnalyzer:
    """
    Log-normal probability estimator for crypto price prediction markets.

    Model (no-drift, martingale approximation):
        P(S_T > K) = 1 - Φ( ln(K / S₀) / (σ_daily × √T) )
        P(S_T < K) = Φ( ln(K / S₀) / (σ_daily × √T) )

    Volatility is derived from the 15-minute ATR supplied by BTCPriceService:
        σ_daily = ATR_15m × √96 / current_price
    (96 fifteen-minute bars per trading day)
    """

    # Map question keywords → canonical asset ticker
    _ASSET_MAP: Dict[str, str] = {
        "bitcoin": "BTC",
        "btc": "BTC",
        "ethereum": "ETH",
        "eth": "ETH",
        "solana": "SOL",
        "sol": "SOL",
    }

    # Direction keywords
    _ABOVE_WORDS = frozenset({"above", "over", "exceed", "exceeds", "reach", "reaches", "hit", "hits", "surpass", "surpasses"})
    _BELOW_WORDS = frozenset({"below", "under", "drop", "drops", "fall", "falls", "dip", "dips"})

    # Sanity bounds per asset (min, max in USD)
    _PRICE_BOUNDS: Dict[str, Tuple[float, float]] = {
        "BTC": (1_000, 10_000_000),
        "ETH": (50, 500_000),
        "SOL": (0.5, 50_000),
    }

    @staticmethod
    def _norm_cdf(x: float) -> float:
        """Standard normal CDF via math.erf (no scipy dependency)."""
        return 0.5 * (1.0 + math.erf(x / math.sqrt(2.0)))

    # ------------------------------------------------------------------
    # Market detection
    # ------------------------------------------------------------------

    def detect_crypto_market(self, question: str) -> Optional[Dict[str, Any]]:
        """
        Parse a market question for a crypto price threshold.

        Returns:
            {'asset': 'BTC'/'ETH'/'SOL', 'target': float, 'direction': 'above'/'below'}
            or None if the question is not a crypto price market.
        """
        q = question.lower()

        # 1. Identify asset
        asset: Optional[str] = None
        for keyword, ticker in self._ASSET_MAP.items():
            if keyword in q:
                asset = ticker
                break
        if asset is None:
            return None

        # 2. Extract price target  ($70,000  |  $70k  |  70000  |  70k)
        target = self._extract_price(q)
        if target is None:
            return None

        # 3. Sanity-check magnitude
        lo, hi = self._PRICE_BOUNDS.get(asset, (1, 10_000_000))
        if not (lo <= target <= hi):
            return None

        # 4. Determine direction (default: above)
        direction = "above"
        for word in self._BELOW_WORDS:
            if word in q:
                direction = "below"
                break

        return {"asset": asset, "target": target, "direction": direction}

    @staticmethod
    def _extract_price(text: str) -> Optional[float]:
        """
        Extract the first dollar-style price from lowercased text.

        Handles: $70,000  $70k  $100K  70000  70k
        Returns the numeric value in USD, or None.
        """
        # Priority 1: explicit $ sign with optional k-multiplier
        m = re.search(r"\$([\d,]+(?:\.\d+)?)\s*([kK])\b", text)
        if m:
            try:
                return float(m.group(1).replace(",", "")) * 1_000
            except ValueError:
                pass

        m = re.search(r"\$([\d,]+(?:\.\d+)?)\b", text)
        if m:
            try:
                return float(m.group(1).replace(",", ""))
            except ValueError:
                pass

        # Priority 2: bare large number (≥4 digits) possibly with k
        m = re.search(r"\b([\d,]{4,})\s*([kK])\b", text)
        if m:
            try:
                return float(m.group(1).replace(",", "")) * 1_000
            except ValueError:
                pass

        m = re.search(r"\b([\d,]{4,}(?:\.\d+)?)\b", text)
        if m:
            try:
                return float(m.group(1).replace(",", ""))
            except ValueError:
                pass

        return None

    # ------------------------------------------------------------------
    # Probability model
    # ------------------------------------------------------------------

    def estimate_probability(
        self,
        current_price: float,
        target_price: float,
        direction: str,
        days_to_expiry: float,
        atr_15m: float,
    ) -> Optional[float]:
        """
        Compute market-implied probability using the log-normal no-drift model.

        Args:
            current_price:  Live spot price in USD.
            target_price:   Threshold from the market question.
            direction:      'above' or 'below'.
            days_to_expiry: Calendar days until market resolution.
            atr_15m:        Current 15-minute ATR in USD (from BTCPriceService).

        Returns:
            Probability in (0.01, 0.99) or None if inputs are invalid.
        """
        if any(v <= 0 for v in (current_price, target_price, days_to_expiry, atr_15m)):
            return None

        # Convert 15m ATR → daily volatility (fractional)
        # σ_daily ≈ ATR_15m × √96 / S₀
        daily_vol = (atr_15m * math.sqrt(96)) / current_price
        # Clamp to plausible range: 0.5% – 20% per day
        daily_vol = max(0.005, min(0.20, daily_vol))

        log_return = math.log(target_price / current_price)
        sigma_sqrt_t = daily_vol * math.sqrt(days_to_expiry)

        if sigma_sqrt_t < 1e-9:
            # Essentially at expiry
            if direction == "above":
                return 0.99 if current_price >= target_price else 0.01
            else:
                return 0.99 if current_price <= target_price else 0.01

        z = log_return / sigma_sqrt_t

        if direction == "above":
            prob = 1.0 - self._norm_cdf(z)
        else:
            prob = self._norm_cdf(z)

        return max(0.01, min(0.99, prob))

    @staticmethod
    def confidence_from_edge(edge: float) -> float:
        """Map raw edge → confidence score for position sizing."""
        if edge >= 0.25:
            return 0.90
        if edge >= 0.18:
            return 0.82
        if edge >= 0.12:
            return 0.74
        return 0.66


